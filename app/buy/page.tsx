    // First, find exact matches and partial matches
    const exactMatches = worldwideDestinations.filter((destination) => destination.toLowerCase().includes(searchLower))

    // If searching for a country name, also show all cities in that country
    const countryMatches = new Set()

    // Check if the search term matches a country name
    const matchingCountries = countries.filter((country) => country.toLowerCase().includes(searchLower))

    // For each matching country, find all destinations in that country
